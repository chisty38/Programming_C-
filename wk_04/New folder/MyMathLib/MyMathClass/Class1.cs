using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace MyMathLib
{
    public class MyMathClass
    {
        public double MySquare(double num)
        {
            return num * num;
        }

        public double MyCube(double num)
        {
            return num * num * num;
        }
    }
